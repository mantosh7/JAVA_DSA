package JAVA_DSA.BASIC;

public class TypeCasting {
    public static void main(String[] args) {
        // implicit
        float a = 12;
        System.out.println(a);

        // explicit
        int b = (int)17.54;
        float c = (int) 20.378;
        System.out.println(b);
        System.out.println(c);
    }
}
