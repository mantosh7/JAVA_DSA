package JAVA_DSA.ARRAY_ARRAYLIST;

public class DefaultValue{
    public static void main(String[] args){
        // default value of integer array
        int[] arr = new int[5];
        System.out.println(arr[0]);  // will give default value as 0

        // default value of String array
        String[] str = new String[5];
        System.out.println(str[0]);  // will give default value as null
    }
}